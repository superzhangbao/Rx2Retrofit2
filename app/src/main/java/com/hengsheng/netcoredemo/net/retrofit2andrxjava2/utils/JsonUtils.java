    /**
     * 把一个map变成json字符串
     *
     * @param map
     * @return
     */
    public static String parseMapToJson(Map<?, ?> map) {
        try {
            return gson.toJson(map);
        } catch (Exception e) {
        }
        return null;
    }

    /**
     * 把一个json字符串变成对象
     *
     * @param json
     * @param cls
     * @return
     */
    public static <T> T parseJsonToBean(String json, Class<T> cls) {
        try {
            if (json == null || cls == null) {
                return null;
            }
            return gson.fromJson(json, cls);
        } catch (Exception e) {
            e.printStackTrace();
            LogUtils.e("数据解析异常");
        }
        return null;
    }

